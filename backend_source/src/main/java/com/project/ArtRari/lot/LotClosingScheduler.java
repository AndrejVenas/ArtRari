package com.project.ArtRari.lot;

import lombok.RequiredArgsConstructor;
import net.javacrumbs.shedlock.spring.annotation.SchedulerLock;
import org.springframework.scheduling.annotation.Scheduled;
import org.springframework.stereotype.Component;

@Component
@RequiredArgsConstructor
public class LotClosingScheduler {
    private final LotService lotService;

    @Scheduled(fixedRate = 10000)
    @SchedulerLock(name = "LotClosingScheduler_closeLots", lockAtLeastFor = "5s", lockAtMostFor = "30s")
    public void closeLots() {
        lotService.closeLots();
    }
}

/*🟢 Категория 1: НИКАКИХ ЛОКОВ (90% всех методов)
Если метод ничего не обновляет, или создает новую запись, локи не нужны. База данных сама прекрасно справится с конкурентностью.

Любые поисковые запросы для отображения (Read-Only):

findAll(), findById() (для страницы лота), findByLotId() (для ленты ставок).

Юзерам не нужен замок, чтобы просто посмотреть на картину. Они могут читать данные параллельно.

Создание новых записей (Inserts):

userRepository.save(newUser), artworkRepository.save(newArtwork).

Пока сущность не попала в базу, конфликта быть не может. За уникальностью email'ов следит ограничение UNIQUE в самой таблице БД, а не Спринг.

Методы планировщика (Поиск целей):

Твои методы findAuctionsToOpen и findAuctionsToClose.

Поскольку у тебя один сервер (монолит), только один поток-планировщик будет их находить и закрывать. Конкурентов у него нет.

🟡 Категория 2: Оптимистичная блокировка (@Version)
Здесь локов на уровне базы (SQL) нет, но есть "паспортный контроль" при сохранении. Используется там, где вероятность конфликта мала, а процесс редактирования долгий.

Редактирование профиля или картин:

Представь, что админ открыл страницу картины, чтобы исправить опечатку в описании. Он может отойти заварить кофе на 10 минут. Если использовать PESSIMISTIC_WRITE, эта строка в БД будет заблокирована 10 минут! Это катастрофа.

Поэтому для методов вроде updateArtwork, updateExhibition или updateUserProfile мы добавляем в сущность поле @Version. Если два админа сохранят картину одновременно, второй получит красивую ошибку: "Данные были изменены другим пользователем".

🔴 Категория 3: Пессимистичная блокировка (@Lock(PESSIMISTIC_WRITE))
Это эксклюзивный замок базы данных SELECT ... FOR UPDATE. Применяется только на критически важных финансовых или бизнес-узлах, где важна каждая миллисекунда и недопустима перезапись.

Подача ставки (placeBid):

Метод: lotRepository.findByIdForUpdate(lotId)

Почему: Конфликты происходят постоянно (снайперы бьются в последние секунды). Нам нужна строгая очередь из потоков.

Закрытие лота (closeLot):

Метод: тоже lotRepository.findByIdForUpdate(lotId)

Почему: Таймер пытается закрыть лот (проверяет, что время вышло), а в эту же миллисекунду залетает ставка снайпера. Если не заблокировать лот, снайпер может поставить ставку на лот, который таймер уже считает закрытым.

Списание/Начисление денег (если будет кошелек):

Метод: walletRepository.findByUserIdForUpdate(userId)

Почему: Чтобы юзер не смог отправить два параллельных запроса на покупку двух картин по 1000$, имея на балансе всего 1000$.*/
